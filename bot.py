# ──────────────────────────────────────────
def question_keyboard(qi: int, shuffled_q: dict, selections: list) -> InlineKeyboardMarkup:
    """Keyboard for a question: option buttons + Done button."""
    selected_set = set(selections[qi]) if qi < len(selections) else set()
    rows = []
    letters = ["А", "Б", "В", "Г"]
    for j, (text, _key) in enumerate(shuffled_q["opts"]):
        check = "✅ " if j in selected_set else ""
        short = text[:55] + "…" if len(text) > 55 else text
        rows.append([InlineKeyboardButton(
            f"{check}{letters[j]}. {short}",
            callback_data=f"pick_{qi}_{j}"
        )])

    # Done button active only if at least one selected
    if selected_set:
        rows.append([InlineKeyboardButton("Далее →", callback_data=f"next_{qi}")])
    else:
        rows.append([InlineKeyboardButton("Выберите хотя бы один вариант", callback_data="noop")])

    return InlineKeyboardMarkup(rows)


def ranking_keyboard(qi: int, selections: list, shuffled_q: dict) -> InlineKeyboardMarkup:
    """Keyboard for ranking selected options by priority."""
    order = selections[qi]
    rows = []
    letters = ["А", "Б", "В", "Г"]
    for pos, j in enumerate(order):
        text = shuffled_q["opts"][j][0]
        short = text[:45] + "…" if len(text) > 45 else text
        row = [
            InlineKeyboardButton(f"{pos+1}. {short}", callback_data="noop"),
        ]
        rows.append(row)
        # Up/Down buttons
        nav_row = []
        if pos > 0:
            nav_row.append(InlineKeyboardButton("▲ Выше", callback_data=f"up_{qi}_{pos}"))
        if pos < len(order) - 1:
            nav_row.append(InlineKeyboardButton("▼ Ниже", callback_data=f"down_{qi}_{pos}"))
        if nav_row:
            rows.append(nav_row)

    rows.append([InlineKeyboardButton("✅ Подтвердить порядок →", callback_data=f"confirm_{qi}")])
    return InlineKeyboardMarkup(rows)


