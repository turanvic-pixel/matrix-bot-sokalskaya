def get_state(context):
    if "quiz" not in context.user_data:
        context.user_data["quiz"] = {"step": 0, "scores": {"S": 0.0, "R": 0.0, "L": 0.0, "A": 0.0}, "shuffled": [], "selections": []}
    return context.user_data["quiz"]


def reset_state(context):
    context.user_data["quiz"] = {"step": 0, "scores": {"S": 0.0, "R": 0.0, "L": 0.0, "A": 0.0}, "shuffled": [], "selections": []}


def question_keyboard(qi, sq, selections):
    selected = set(selections[qi]) if qi < len(selections) else set()
    letters = ["А", "Б", "В", "Г"]
    rows = []
    for j, (text, _) in enumerate(sq["opts"]):
        check = "✅ " if j in selected else ""
        short = text[:55] + "…" if len(text) > 55 else text
        rows.append([InlineKeyboardButton(f"{check}{letters[j]}. {short}", callback_data=f"pick_{qi}_{j}")])
    if selected:
        rows.append([InlineKeyboardButton("Далее →", callback_data=f"next_{qi}")])
    else:
        rows.append([InlineKeyboardButton("Выберите хотя бы один вариант", callback_data="noop")])
    return InlineKeyboardMarkup(rows)


def ranking_keyboard(qi, selections, sq):
    order = selections[qi]
    rows = []
    for pos, j in enumerate(order):
        text = sq["opts"][j][0]
        short = text[:45] + "…" if len(text) > 45 else text
        rows.append([InlineKeyboardButton(f"{pos+1}. {short}", callback_data="noop")])
        nav = []
        if pos > 0:
            nav.append(InlineKeyboardButton("▲ Выше", callback_data=f"up_{qi}_{pos}"))
        if pos < len(order) - 1:
            nav.append(InlineKeyboardButton("▼ Ниже", callback_data=f"down_{qi}_{pos}"))
        if nav:
            rows.append(nav)
    rows.append([InlineKeyboardButton("✅ Подтвердить порядок →", callback_data=f"confirm_{qi}")])
    return InlineKeyboardMarkup(rows)


def format_question(qi, sq):
    letters = ["А", "Б", "В", "Г"]
    opts = "\n".join(f"{letters[j]}. {o[0]}" for j, o in enumerate(sq["opts"]))
    return f"*Вопрос {qi+1} из {len(QUESTIONS)}*\n_{sq['cat']}_\n\n*{sq['q']}*\n\n{opts}\n\n_Можно выбрать несколько вариантов_"


async def send_question(query, context, qi):
    state = get_state(context)
    state["step"] = qi
    sq = state["shuffled"][qi]
    text = format_question(qi, sq)
    kb = question_keyboard(qi, sq, state["selections"])
    try:
        await query.edit_message_text(text, parse_mode="Markdown", reply_markup=kb)
    except Exception:
        await query.message.reply_text(text, parse_mode="Markdown", reply_markup=kb)


async def advance(query, context, qi):
    state = get_state(context)
    sel = state["selections"][qi]
    sq = state["shuffled"][qi]
    total = len(sel)
    for pos, j in enumerate(sel):
        weight = round((total - pos) / total, 1) if total > 1 else 1.0
        key = sq["opts"][j][1]
        state["scores"][key] += weight
    next_qi = qi + 1
    if next_qi < len(QUESTIONS):
        await send_question(query, context, next_qi)
    else:
        await show_result(query, context)


async def show_result(query, context):
    state = get_state(context)
    scores = state["scores"]
    total = sum(scores.values()) or 1
    winner = max(scores, key=scores.get)
    m = MATRIX[winner]
    order = ["S", "R", "L", "A"]
    bars = ""
    for k in order:
        pct = round(scores[k] / total * 100)
        filled = round(pct / 5)
        bar = "█" * filled + "░" * (20 - filled)
        bars += f"{MATRIX[k]['name'].split()[0]:<7} {bar} {pct}%\n"
    traits = "\n".join(f"• {t}" for t in m["traits"])
    text = (f"🎯 *Ваша матрица: {m['name']}*\n\n_{m['deviz']}_\n\n{m['sub']}\n\n"
            f"*📊 Распределение:*\n```\n{bars}```\n*✦ Ключевые признаки:*\n{traits}")
    kb = InlineKeyboardMarkup([
        [InlineKeyboardButton("📋 Рекомендации", callback_data=f"recs_{winner}")],
        [InlineKeyboardButton("⚠️ Ловушки", callback_data=f"warns_{winner}")],
        [InlineKeyboardButton("🔄 Пройти снова", callback_data="restart")],
    ])
    context.user_data["winner"] = winner
    try:
        await query.edit_message_text(text, parse_mode="Markdown", reply_markup=kb)
    except Exception:
        await query.message.reply_text(text, parse_mode="Markdown", reply_markup=kb)


async def start(update: Update, context: ContextTypes.DEFAULT_TYPE):
    reset_state(context)
    state = get_state(context)
    for q in QUESTIONS:
        opts = list(q["opts"])
        random.shuffle(opts)
        state["shuffled"].append({"cat": q["cat"], "q": q["q"], "opts": opts})
    state["selections"] = [[] for _ in QUESTIONS]
    text = ("🌱 *Тест: Энергетическая матрица*\n\n"
            "Тест построен на авторском материале *Сокальской Екатерины Александровны*.\n\n"
            "Особенности души сложно оцифровать — результаты могут не совпадать с вашими "
            "личными ощущениями. *Слушайте в первую очередь себя.*\n\n"
            f"Всего *{len(QUESTIONS)} вопросов*. Можно выбирать несколько вариантов.")
    kb = InlineKeyboardMarkup([[InlineKeyboardButton("🚀 Начать тест", callback_data="begin")]])
    await update.message.reply_text(text, parse_mode="Markdown", reply_markup=kb)


async def button_handler(update: Update, context: ContextTypes.DEFAULT_TYPE):
    query = update.callback_query
    await query.answer()
    data = query.data
    state = get_state(context)

    if data == "noop":
        return

    if data == "begin":
        await send_question(query, context, 0)
        return

    if data == "restart":
        reset_state(context)
        state = get_state(context)
        for q in QUESTIONS:
            opts = list(q["opts"])
            random.shuffle(opts)
            state["shuffled"].append({"cat": q["cat"], "q": q["q"], "opts": opts})
        state["selections"] = [[] for _ in QUESTIONS]
        await send_question(query, context, 0)
        return

    if data.startswith("pick_"):
        _, qi_s, j_s = data.split("_")
        qi, j = int(qi_s), int(j_s)
        sel = state["selections"][qi]
        if j in sel:
            sel.remove(j)
        else:
            sel.append(j)
        sq = state["shuffled"][qi]
        await query.edit_message_text(format_question(qi, sq), parse_mode="Markdown",
                                      reply_markup=question_keyboard(qi, sq, state["selections"]))
        return

    if data.startswith("next_"):
        qi = int(data.split("_")[1])
        if len(state["selections"][qi]) >= 2:
            sq = state["shuffled"][qi]
            text = (f"*Вопрос {qi+1} — расставьте в порядке важности*\n_{sq['cat']}_\n\n"
                    "Самый важный — первым. Нажимайте ▲ ▼ для перестановки:")
            await query.edit_message_text(text, parse_mode="Markdown",
                                          reply_markup=ranking_keyboard(qi, state["selections"], sq))
        else:
            await advance(query, context, qi)
        return

    if data.startswith("up_"):
        _, qi_s, pos_s = data.split("_")
        qi, pos = int(qi_s), int(pos_s)
        sel = state["selections"][qi]
        if pos > 0:
            sel[pos], sel[pos-1] = sel[pos-1], sel[pos]
        sq = state["shuffled"][qi]
        text = f"*Вопрос {qi+1} — расставьте в порядке важности*\n_{sq['cat']}_\n\nСамый важный — первым:"
        await query.edit_message_text(text, parse_mode="Markdown",
                                      reply_markup=ranking_keyboard(qi, state["selections"], sq))
        return

    if data.startswith("down_"):
        _, qi_s, pos_s = data.split("_")
        qi, pos = int(qi_s), int(pos_s)
        sel = state["selections"][qi]
        if pos < len(sel) - 1:
            sel[pos], sel[pos+1] = sel[pos+1], sel[pos]
        sq = state["shuffled"][qi]
        text = f"*Вопрос {qi+1} — расставьте в порядке важности*\n_{sq['cat']}_\n\nСамый важный — первым:"
        await query.edit_message_text(text, parse_mode="Markdown",
                                      reply_markup=ranking_keyboard(qi, state["selections"], sq))
        return

    if data.startswith("confirm_"):
        qi = int(data.split("_")[1])
        await advance(query, context, qi)
        return

    if data.startswith("recs_"):
        key = data.split("_")[1]
        m = MATRIX[key]
        text = f"*Рекомендации для матрицы {m['name']}*\n\n"
        for i, (title, body) in enumerate(m["recs"], 1):
            text += f"*{i}. {title}*\n{body}\n\n"
        kb = InlineKeyboardMarkup([
            [InlineKeyboardButton("⚠️ Ловушки", callback_data=f"warns_{key}")],
            [InlineKeyboardButton("← Назад к результату", callback_data=f"result_{key}")],
        ])
        try:
            await query.edit_message_text(text, parse_mode="Markdown", reply_markup=kb)
        except Exception:
            await query.message.reply_text(text, parse_mode="Markdown", reply_markup=kb)
        return

    if data.startswith("warns_"):
        key = data.split("_")[1]
        m = MATRIX[key]
        text = f"*Ловушки матрицы {m['name']}*\n\n"
        for title, body in m["warns"]:
            text += f"⚠️ *{title}*\n{body}\n\n"
        kb = InlineKeyboardMarkup([
            [InlineKeyboardButton("📋 Рекомендации", callback_data=f"recs_{key}")],
            [InlineKeyboardButton("← Назад к результату", callback_data=f"result_{key}")],
        ])
        try:
            await query.edit_message_text(text, parse_mode="Markdown", reply_markup=kb)
        except Exception:
            await query.message.reply_text(text, parse_mode="Markdown", reply_markup=kb)
        return

    if data.startswith("result_"):
        await show_result(query, context)
        return


def main():
    app = Application.builder().token(TOKEN).build()
    app.add_handler(CommandHandler("start", start))
    app.add_handler(CallbackQueryHandler(button_handler))
    print("Бот запущен!")
    app.run_polling(drop_pending_updates=True)


if __name__ == "__main__":
    main()
