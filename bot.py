def get_state(context: ContextTypes.DEFAULT_TYPE) -> dict:
    if "quiz" not in context.user_data:
        context.user_data["quiz"] = {
            "step": 0,
            "scores": {"S": 0.0, "R": 0.0, "L": 0.0, "A": 0.0},
            "shuffled": [],
            "selections": [],  # list of lists per question
        }
    return context.user_data["quiz"]


def reset_state(context: ContextTypes.DEFAULT_TYPE):
    context.user_data["quiz"] = {
        "step": 0,
        "scores": {"S": 0.0, "R": 0.0, "L": 0.0, "A": 0.0},
        "shuffled": [],
        "selections": [],
    }


