async def start(update: Update, context: ContextTypes.DEFAULT_TYPE):
    reset_state(context)
    state = get_state(context)

    # Shuffle options for each question
    for q in QUESTIONS:
        opts = list(q["opts"])
        random.shuffle(opts)
        state["shuffled"].append({"cat": q["cat"], "q": q["q"], "opts": opts})

    state["selections"] = [[] for _ in QUESTIONS]

    text = (
        "🌱 *Тест по определению энергетической матрицы*\n\n"
        "Данный тест построен на авторском материале *Сокальской Екатерины Александровны*.\n\n"
        "Особенности и тонкости души сложно оцифровать, поэтому результаты теста могут не совпадать "
        "с вашими личными ощущениями. *Слушайте в первую очередь себя.*\n\n"
        f"Всего вопросов: *{len(QUESTIONS)}*\n"
        "Можно выбирать несколько вариантов на каждый вопрос.\n\n"
        "Нажмите кнопку чтобы начать 👇"
    )
    keyboard = InlineKeyboardMarkup([[
        InlineKeyboardButton("🚀 Начать тест", callback_data="begin")
    ]])
    await update.message.reply_text(text, parse_mode="Markdown", reply_markup=keyboard)


async def button_handler(update: Update, context: ContextTypes.DEFAULT_TYPE):
    query = update.callback_query
    await query.answer()
    data = query.data
    state = get_state(context)

    # ── begin ──
    if data == "begin":
        await send_question(query, context, 0)
        return

    # ── noop ──
    if data == "noop":
        return

    # ── pick option ──
    if data.startswith("pick_"):
        _, qi_s, j_s = data.split("_")
        qi, j = int(qi_s), int(j_s)
        sel = state["selections"][qi]
        if j in sel:
            sel.remove(j)
        else:
            sel.append(j)
        state["step"] = qi
        sq = state["shuffled"][qi]
        text = format_question(qi, sq)
        kb = question_keyboard(qi, sq, state["selections"])
        await query.edit_message_text(text, parse_mode="Markdown", reply_markup=kb)
        return

    # ── next / go to ranking or next question ──
    if data.startswith("next_"):
        qi = int(data.split("_")[1])
        sel = state["selections"][qi]
        if len(sel) >= 2:
            # Show ranking step
            sq = state["shuffled"][qi]
            text = (
                f"*Вопрос {qi+1} из {len(QUESTIONS)} — расставьте в порядке важности*\n\n"
                f"_{sq['cat']}_\n\n"
                "Самый важный вариант должен быть первым.\n"
                "Используйте ▲ ▼ для перестановки:"
            )
            kb = ranking_keyboard(qi, state["selections"], sq)
            await query.edit_message_text(text, parse_mode="Markdown", reply_markup=kb)
        else:
            # Single selection — go to next question directly
            await advance(query, context, qi)
        return

    # ── move up ──
    if data.startswith("up_"):
        _, qi_s, pos_s = data.split("_")
        qi, pos = int(qi_s), int(pos_s)
        sel = state["selections"][qi]
        if pos > 0:
            sel[pos], sel[pos-1] = sel[pos-1], sel[pos]
        sq = state["shuffled"][qi]
        text = (
            f"*Вопрос {qi+1} из {len(QUESTIONS)} — расставьте в порядке важности*\n\n"
            f"_{sq['cat']}_\n\nСамый важный вариант должен быть первым:"
        )
        kb = ranking_keyboard(qi, state["selections"], sq)
        await query.edit_message_text(text, parse_mode="Markdown", reply_markup=kb)
        return

    # ── move down ──
    if data.startswith("down_"):
        _, qi_s, pos_s = data.split("_")
        qi, pos = int(qi_s), int(pos_s)
        sel = state["selections"][qi]
        if pos < len(sel) - 1:
            sel[pos], sel[pos+1] = sel[pos+1], sel[pos]
        sq = state["shuffled"][qi]
        text = (
            f"*Вопрос {qi+1} из {len(QUESTIONS)} — расставьте в порядке важности*\n\n"
            f"_{sq['cat']}_\n\nСамый важный вариант должен быть первым:"
        )
        kb = ranking_keyboard(qi, state["selections"], sq)
        await query.edit_message_text(text, parse_mode="Markdown", reply_markup=kb)
        return

    # ── confirm ranking → advance ──
    if data.startswith("confirm_"):
        qi = int(data.split("_")[1])
        await advance(query, context, qi)
        return

    # ── restart ──
    if data == "restart":
        reset_state(context)
        state = get_state(context)
        for q in QUESTIONS:
            opts = list(q["opts"])
            random.shuffle(opts)
            state["shuffled"].append({"cat": q["cat"], "q": q["q"], "opts": opts})
        state["selections"] = [[] for _ in QUESTIONS]
        await send_question(query, context, 0)
        return


async def advance(query, context, qi):
    """Apply scores for question qi and move to next question or show result."""
    state = get_state(context)
    sel = state["selections"][qi]
    sq = state["shuffled"][qi]
    total = len(sel)
    for pos, j in enumerate(sel):
        weight = round((total - pos) / total, 1) if total > 1 else 1.0
        key = sq["opts"][j][1]
        state["scores"][key] += weight

    next_qi = qi + 1
    if next_qi < len(QUESTIONS):
        await send_question(query, context, next_qi)
    else:
        await show_result(query, context)


async def send_question(query, context, qi: int):
    state = get_state(context)
    state["step"] = qi
    sq = state["shuffled"][qi]
    text = format_question(qi, sq)
    kb = question_keyboard(qi, sq, state["selections"])
    try:
        await query.edit_message_text(text, parse_mode="Markdown", reply_markup=kb)
    except Exception:
        await query.message.reply_text(text, parse_mode="Markdown", reply_markup=kb)


def format_question(qi: int, sq: dict) -> str:
    letters = ["А", "Б", "В", "Г"]
    opts_text = "\n".join(
        f"{letters[j]}. {opt[0]}" for j, opt in enumerate(sq["opts"])
    )
    return (
        f"*Вопрос {qi+1} из {len(QUESTIONS)}*\n"
        f"_{sq['cat']}_\n\n"
        f"*{sq['q']}*\n\n"
        f"{opts_text}\n\n"
        "_(Можно выбрать несколько вариантов — отметьте все подходящие)_"
    )


async def show_result(query, context):
    state = get_state(context)
    scores = state["scores"]
    total = sum(scores.values()) or 1
    sorted_scores = sorted(scores.items(), key=lambda x: x[1], reverse=True)
    winner_key = sorted_scores[0][0]
    m = MATRIX[winner_key]

    # Build bar chart
    bars = ""
    for k in MATRIX_ORDER:
        pct = round(scores[k] / total * 100)
        filled = round(pct / 5)
        bar = "█" * filled + "░" * (20 - filled)
        bars += f"{MATRIX[k]['emoji']} {MATRIX[k]['name'].split()[0]:<7} {bar} {pct}%\n"

    traits = "\n".join(f"• {t}" for t in m["traits"])

    text = (
        f"🎯 *Ваша матрица: {m['name']}*\n\n"
        f"_{m['deviz']}_\n\n"
        f"{m['sub']}\n\n"
        f"*📊 Распределение ответов:*\n"
        f"```\n{bars}```\n"
        f"*✦ Ключевые признаки:*\n{traits}"
    )

    kb = InlineKeyboardMarkup([
        [InlineKeyboardButton("📋 Рекомендации", callback_data=f"recs_{winner_key}")],
        [InlineKeyboardButton("⚠️ Ловушки матрицы", callback_data=f"warns_{winner_key}")],
        [InlineKeyboardButton("🔄 Пройти снова", callback_data="restart")],
    ])

    try:
        await query.edit_message_text(text, parse_mode="Markdown", reply_markup=kb)
    except Exception:
        await query.message.reply_text(text, parse_mode="Markdown", reply_markup=kb)

    # Store winner for sub-pages
    context.user_data["winner"] = winner_key


